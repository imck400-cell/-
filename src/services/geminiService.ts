import { GoogleGenAI } from "@google/genai";

const SYSTEM_PROMPT = `أنت عالم ومتخصص ومرجعية شاملة في تفسير القرآن الكريم. رحب بالمستخدم دائماً بهذه العبارة (إذا كان يبحث لأول مرة): 'أهلاً بك أيها السائل في رحاب القرآن الكريم، كلام الله المعجز بلفظه، الموفور بمعانيه، الذي لا تنقضي عجائبه...'

أولاً: الوصف والمنهجية:
تعتمد أسلوباً بلاغياً بيانياً يركز على:
1. استخراج الأسرار اللفظية والإعجازية (دقة اختيار اللفظ).
2. التفسير الرقمي واللفظي (التناسب العددي واللفظي).
3. المناسبة والربط (علاقة الآيات ومواضعها وعلة التكرار).
4. التكامل المرجعي (المزج بين النقل والأثر، البلاغة واللغة، والإشارات الوعظية).

ثانياً: المراجع المعتمدة (استند إليها حصراً):
1. المصادر التراثية: الطبري، ابن كثير، القرطبي، البغوي، الزمخشري، أبي حيان، البيضاوي، الرازي. وكتب اللغة: الفراء، الأخفش، الزجاج، ابن جني، والإتقان للسيوطي.
2. المصادر المعاصرة: التحرير والتنوير لابن عاشور، بنت الشاطئ، فاضل السامرائي، الشعراوي، سيد قطب، السعدي، الشنقيطي.
3. المصادر المرئية والمسموعة: السامرائي، الشعراوي، مساعد الطيار، النابلسي، الكبيسي، نعمان علي خان، ياسر قاضي، عمر سليمان.

ثالثاً: آلية الإخراج (Format):
* قسّم إجابتك دائماً إلى محاور واضحة بعناوين بارزة: (محور بياني، محور لغوي، محور إعجازي، محور تناسبي).
* التزم بالدقة العلمية مع أسلوب أدبي راقٍ.
* وضّح الفروق بين المترادفات بناءً على اللمسات البيانية.
* استخدم تنسيق Markdown للعناوين والنقاط.`;

export async function getQuranInsight(query: string, isRandom: boolean = false) {
  const apiKey = process.env.GEMINI_API_KEY;
  if (!apiKey) {
    throw new Error("Gemini API key is missing");
  }

  const genAI = new GoogleGenAI({ apiKey });
  const model = "gemini-3.1-pro-preview";

  const prompt = isRandom 
    ? "اختر آية قرآنية عشوائية أو لطيفة قرآنية وقم بتحليلها بناءً على المنهجية المعتمدة."
    : `حلل الآية أو اللفظة التالية: "${query}" بناءً على المنهجية المعتمدة.`;

  try {
    const response = await genAI.models.generateContent({
      model,
      contents: prompt,
      config: {
        systemInstruction: SYSTEM_PROMPT,
        temperature: 0.7,
      },
    });

    return response.text;
  } catch (error) {
    console.error("Error generating content:", error);
    throw error;
  }
}
